package HomeWork16;

public class MathFunction {

    public int sumOfValues(int a, int b){
        int sumAB = a + b;
        return sumAB;
    }

    public int MultiplyOfValues (int a, int b) {
        int multAB = a * b;
        return multAB;
    }

    public double SumOfDoubles (double a, double b){
        double sumDoubles = a + b;
        return sumDoubles;
    }

    public double MultiplyDoubles (double a, double b) {
        double multDoubles = a * b;
        return multDoubles;
    }

}
